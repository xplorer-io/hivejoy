// Helper to create variants
const createVariants = (
  productId: string,
  variantIds: [string, string, string],
  basePrice: number,
  stock: number,
): ProductVariant[] => [
  {
    id: variantIds[0],
    productId,
    size: '250g',
    price: basePrice,
    stock,
    weight: 250,
  },
  {
    id: variantIds[1],
    productId,
    size: '500g',
    price: basePrice * 1.8,
    stock: Math.floor(stock * 0.8),
    weight: 500,
  },
  {
    id: variantIds[2],
    productId,
    size: '1kg',
    price: basePrice * 3.2,
    stock: Math.floor(stock * 0.5),
    weight: 1000,
  },
];

